-- Secuencias compatibles con Oracle requeridas por las entidades JPA
CREATE SEQUENCE IF NOT EXISTS SEQ_ROL              START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_USUARIO          START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_ESPECIALIDAD     START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_PACIENTE         START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_MEDICO           START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_HORARIO_MEDICO   START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_CITA             START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_HISTORIA_CLINICA START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_ATENCION         START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_RECETA           START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_DETALLE_RECETA   START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_ORDEN_EXAMEN     START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_PAGO             START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_AUDITORIA_LOG    START WITH 100 INCREMENT BY 1;
CREATE SEQUENCE IF NOT EXISTS SEQ_INTENTO_LOGIN    START WITH 100 INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS ROL (
    ID_ROL      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE      VARCHAR(30)  NOT NULL UNIQUE,
    DESCRIPCION VARCHAR(200),
    ACTIVO      INT          DEFAULT 1 NOT NULL,
    CONSTRAINT CK_ROL_ACTIVO CHECK (ACTIVO IN (0,1))
);

CREATE TABLE IF NOT EXISTS USUARIO (
    ID_USUARIO        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME          VARCHAR(50)  NOT NULL UNIQUE,
    PASSWORD_HASH     VARCHAR(255) NOT NULL,
    EMAIL             VARCHAR(120) NOT NULL UNIQUE,
    ACTIVO            INT          DEFAULT 1 NOT NULL,
    INTENTOS_FALLIDOS INT          DEFAULT 0 NOT NULL,
    BLOQUEADO         INT          DEFAULT 0 NOT NULL,
    FECHA_CREACION    TIMESTAMP    DEFAULT CURRENT_TIMESTAMP NOT NULL,
    FECHA_ULT_LOGIN   TIMESTAMP
);

CREATE TABLE IF NOT EXISTS USUARIO_ROL (
    ID_USUARIO BIGINT NOT NULL,
    ID_ROL     BIGINT NOT NULL,
    CONSTRAINT PK_USUARIO_ROL PRIMARY KEY (ID_USUARIO, ID_ROL),
    CONSTRAINT FK_UR_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO) ON DELETE CASCADE,
    CONSTRAINT FK_UR_ROL     FOREIGN KEY (ID_ROL)     REFERENCES ROL(ID_ROL)
);

CREATE TABLE IF NOT EXISTS ESPECIALIDAD (
    ID_ESPECIALIDAD  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE           VARCHAR(80)    NOT NULL UNIQUE,
    DESCRIPCION      VARCHAR(300),
    PRECIO_CONSULTA  DECIMAL(10,2)  NOT NULL,
    DURACION_MINUTOS INT            DEFAULT 20 NOT NULL,
    ACTIVO           INT            DEFAULT 1  NOT NULL,
    CONSTRAINT CK_ESPEC_PRECIO   CHECK (PRECIO_CONSULTA >= 0),
    CONSTRAINT CK_ESPEC_DURACION CHECK (DURACION_MINUTOS BETWEEN 5 AND 240),
    CONSTRAINT CK_ESPEC_ACTIVO   CHECK (ACTIVO IN (0,1))
);

CREATE TABLE IF NOT EXISTS PACIENTE (
    ID_PACIENTE      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_USUARIO       BIGINT,
    DNI              VARCHAR(8)   NOT NULL UNIQUE,
    NOMBRES          VARCHAR(80)  NOT NULL,
    APELLIDO_PATERNO VARCHAR(50)  NOT NULL,
    APELLIDO_MATERNO VARCHAR(50),
    FECHA_NACIMIENTO DATE         NOT NULL,
    SEXO             CHAR(1)      NOT NULL,
    TELEFONO         VARCHAR(15),
    EMAIL            VARCHAR(120),
    DIRECCION        VARCHAR(200),
    DISTRITO         VARCHAR(80),
    TIPO_SANGRE      VARCHAR(5),
    ALERGIAS         VARCHAR(500),
    ACTIVO           INT          DEFAULT 1 NOT NULL,
    FECHA_REGISTRO   TIMESTAMP    DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_PAC_USUARIO  FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT CK_PACIENTE_SEXO   CHECK (SEXO IN ('M','F')),
    CONSTRAINT CK_PACIENTE_ACTIVO CHECK (ACTIVO IN (0,1))
);

CREATE TABLE IF NOT EXISTS MEDICO (
    ID_MEDICO        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_USUARIO       BIGINT       NOT NULL UNIQUE,
    ID_ESPECIALIDAD  BIGINT       NOT NULL,
    CMP              VARCHAR(15)  NOT NULL UNIQUE,
    NOMBRES          VARCHAR(80)  NOT NULL,
    APELLIDO_PATERNO VARCHAR(50)  NOT NULL,
    APELLIDO_MATERNO VARCHAR(50),
    DNI              VARCHAR(8)   NOT NULL UNIQUE,
    TELEFONO         VARCHAR(15),
    EMAIL            VARCHAR(120),
    CONSULTORIO      VARCHAR(20),
    ACTIVO           INT          DEFAULT 1 NOT NULL,
    CONSTRAINT FK_MED_USUARIO FOREIGN KEY (ID_USUARIO)      REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT FK_MED_ESPEC   FOREIGN KEY (ID_ESPECIALIDAD) REFERENCES ESPECIALIDAD(ID_ESPECIALIDAD),
    CONSTRAINT CK_MEDICO_ACTIVO CHECK (ACTIVO IN (0,1))
);

CREATE TABLE IF NOT EXISTS HORARIO_MEDICO (
    ID_HORARIO  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_MEDICO   BIGINT      NOT NULL,
    DIA_SEMANA  INT         NOT NULL,
    HORA_INICIO VARCHAR(5)  NOT NULL,
    HORA_FIN    VARCHAR(5)  NOT NULL,
    ACTIVO      INT         DEFAULT 1 NOT NULL,
    CONSTRAINT FK_HOR_MEDICO    FOREIGN KEY (ID_MEDICO) REFERENCES MEDICO(ID_MEDICO) ON DELETE CASCADE,
    CONSTRAINT CK_HOR_DIA       CHECK (DIA_SEMANA BETWEEN 1 AND 7),
    CONSTRAINT CK_HOR_ACTIVO    CHECK (ACTIVO IN (0,1)),
    CONSTRAINT UQ_HORARIO_UNICO UNIQUE (ID_MEDICO, DIA_SEMANA, HORA_INICIO)
);

CREATE TABLE IF NOT EXISTS HISTORIA_CLINICA (
    ID_HISTORIA    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_PACIENTE    BIGINT       NOT NULL UNIQUE,
    NRO_HISTORIA   VARCHAR(15)  NOT NULL UNIQUE,
    FECHA_APERTURA DATE         DEFAULT CURRENT_DATE NOT NULL,
    OBSERVACIONES  VARCHAR(1000),
    ACTIVO         INT          DEFAULT 1 NOT NULL,
    CONSTRAINT FK_HC_PACIENTE FOREIGN KEY (ID_PACIENTE) REFERENCES PACIENTE(ID_PACIENTE) ON DELETE CASCADE,
    CONSTRAINT CK_HC_ACTIVO CHECK (ACTIVO IN (0,1))
);

CREATE TABLE IF NOT EXISTS DIAGNOSTICO_CIE10 (
    CODIGO      VARCHAR(10)  PRIMARY KEY,
    DESCRIPCION VARCHAR(300) NOT NULL,
    CAPITULO    VARCHAR(100),
    ACTIVO      INT          DEFAULT 1 NOT NULL
);

CREATE TABLE IF NOT EXISTS CITA (
    ID_CITA         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_PACIENTE     BIGINT       NOT NULL,
    ID_MEDICO       BIGINT       NOT NULL,
    ID_ESPECIALIDAD BIGINT       NOT NULL,
    FECHA_CITA      DATE         NOT NULL,
    HORA_INICIO     VARCHAR(5)   NOT NULL,
    HORA_FIN        VARCHAR(5)   NOT NULL,
    ESTADO          VARCHAR(20)  DEFAULT 'PROGRAMADA' NOT NULL,
    MOTIVO_CONSULTA VARCHAR(300),
    CANAL_RESERVA   VARCHAR(20)  DEFAULT 'WEB' NOT NULL,
    CODIGO_QR       VARCHAR(100),
    NUMERO_TURNO    INT,
    FECHA_REGISTRO  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP NOT NULL,
    FECHA_CHECKIN   TIMESTAMP,
    OBSERVACIONES   VARCHAR(500),
    CONSTRAINT FK_CITA_PACIENTE     FOREIGN KEY (ID_PACIENTE)     REFERENCES PACIENTE(ID_PACIENTE),
    CONSTRAINT FK_CITA_MEDICO       FOREIGN KEY (ID_MEDICO)       REFERENCES MEDICO(ID_MEDICO),
    CONSTRAINT FK_CITA_ESPECIALIDAD FOREIGN KEY (ID_ESPECIALIDAD) REFERENCES ESPECIALIDAD(ID_ESPECIALIDAD),
    CONSTRAINT UQ_CITA_SLOT UNIQUE (ID_MEDICO, FECHA_CITA, HORA_INICIO)
);

CREATE TABLE IF NOT EXISTS ATENCION (
    ID_ATENCION         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_CITA             BIGINT UNIQUE NOT NULL,
    ID_HISTORIA         BIGINT NOT NULL,
    ID_MEDICO           BIGINT NOT NULL,
    FECHA_ATENCION      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    MOTIVO_CONSULTA     VARCHAR(500),
    ANAMNESIS           CLOB,
    EXAMEN_FISICO       CLOB,
    DIAGNOSTICO_CIE10   VARCHAR(10),
    DIAGNOSTICO_DESC    VARCHAR(500),
    TRATAMIENTO         CLOB,
    OBSERVACIONES       VARCHAR(1000),
    PRESION_ARTERIAL    VARCHAR(15),
    FRECUENCIA_CARDIACA INT,
    TEMPERATURA         DECIMAL(4,1),
    PESO_KG             DECIMAL(5,2),
    TALLA_CM            DECIMAL(5,2),
    SATURACION_O2       INT,
    FIRMADO_POR         BIGINT,
    FECHA_FIRMA         TIMESTAMP,
    CONSTRAINT FK_ATEN_CITA       FOREIGN KEY (ID_CITA)           REFERENCES CITA(ID_CITA),
    CONSTRAINT FK_ATEN_HISTORIA   FOREIGN KEY (ID_HISTORIA)       REFERENCES HISTORIA_CLINICA(ID_HISTORIA),
    CONSTRAINT FK_ATEN_MEDICO     FOREIGN KEY (ID_MEDICO)         REFERENCES MEDICO(ID_MEDICO),
    CONSTRAINT FK_ATEN_CIE10      FOREIGN KEY (DIAGNOSTICO_CIE10) REFERENCES DIAGNOSTICO_CIE10(CODIGO),
    CONSTRAINT FK_ATEN_USUARIO    FOREIGN KEY (FIRMADO_POR)       REFERENCES USUARIO(ID_USUARIO)
);

CREATE TABLE IF NOT EXISTS RECETA (
    ID_RECETA    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_ATENCION  BIGINT       NOT NULL,
    NRO_RECETA   VARCHAR(20)  NOT NULL UNIQUE,
    FECHA_EMISION TIMESTAMP   DEFAULT CURRENT_TIMESTAMP,
    OBSERVACIONES VARCHAR(300),
    CONSTRAINT FK_REC_ATENCION FOREIGN KEY (ID_ATENCION) REFERENCES ATENCION(ID_ATENCION) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS DETALLE_RECETA (
    ID_DETALLE         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_RECETA          BIGINT       NOT NULL,
    NOMBRE_GENERICO    VARCHAR(200) NOT NULL,
    NOMBRE_COMERCIAL   VARCHAR(200),
    PRESENTACION       VARCHAR(100),
    DOSIS              VARCHAR(100) NOT NULL,
    FRECUENCIA         VARCHAR(100) NOT NULL,
    DURACION_DIAS      INT          NOT NULL,
    VIA_ADMINISTRACION VARCHAR(50),
    INDICACIONES       VARCHAR(300),
    CONSTRAINT FK_DR_RECETA FOREIGN KEY (ID_RECETA) REFERENCES RECETA(ID_RECETA) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS ORDEN_EXAMEN (
    ID_ORDEN        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_ATENCION     BIGINT       NOT NULL,
    TIPO_EXAMEN     VARCHAR(50)  NOT NULL,
    NOMBRE_EXAMEN   VARCHAR(200) NOT NULL,
    INDICACIONES    CLOB,
    ESTADO          VARCHAR(20)  DEFAULT 'SOLICITADO',
    FECHA_SOLICITUD TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    FECHA_RESULTADO TIMESTAMP,
    CONSTRAINT FK_OE_ATENCION FOREIGN KEY (ID_ATENCION) REFERENCES ATENCION(ID_ATENCION) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS PAGO (
    ID_PAGO          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_CITA          BIGINT         NOT NULL,
    MONTO            DECIMAL(10,2)  NOT NULL,
    METODO_PAGO      VARCHAR(20)    NOT NULL,
    ESTADO           VARCHAR(20)    DEFAULT 'PENDIENTE' NOT NULL,
    NRO_COMPROBANTE  VARCHAR(30),
    TIPO_COMPROBANTE VARCHAR(15),
    FECHA_PAGO       TIMESTAMP,
    FECHA_REGISTRO   TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_PAGO_CITA FOREIGN KEY (ID_CITA) REFERENCES CITA(ID_CITA)
);

CREATE TABLE IF NOT EXISTS AUDITORIA_LOG (
    ID_LOG     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_USUARIO BIGINT,
    USERNAME   VARCHAR(50),
    ACCION     VARCHAR(50)  NOT NULL,
    ENTIDAD    VARCHAR(50),
    ID_ENTIDAD VARCHAR(50),
    DETALLE    VARCHAR(500),
    IP_ORIGEN  VARCHAR(45),
    FECHA_HORA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_AUDIT_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO)
);

CREATE TABLE IF NOT EXISTS INTENTO_LOGIN (
    ID_INTENTO    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME      VARCHAR(50)  NOT NULL,
    EXITOSO       INT          DEFAULT 0 NOT NULL,
    IP_ORIGEN     VARCHAR(45),
    MENSAJE_ERROR VARCHAR(255),
    FECHA_HORA    TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
);
